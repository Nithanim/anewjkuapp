package org.voidsink.kussslib;

/**
 * Enumeration contains all possible events in the KUSSS, namely:
 * - an EXAM event
 * - and COURSE event
 * 
 * Events in this context are specific information or news which concern either an exam or a course.
 * 
 * @author Paul Pretsch, Lukas Habring, Michaela Schoenbauer
 * @version 1.0
 *
 */
public enum EventType {
	EXAM, COURSE
}
